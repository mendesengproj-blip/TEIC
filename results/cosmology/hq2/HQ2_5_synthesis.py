"""HQ2_5_synthesis.py -- honest synthesis + pre-registered verdict for HQ2.

Reads the HQ2-V .. HQ2-4 JSON artifacts and writes HQ2_5_synthesis.md with the
charter's synthesis table and the verdict, decided strictly by the pre-registered
criteria in HQ2_CRITICAL_FERROMAGNET.md (sec. 5).  No new physics here.
"""
from __future__ import annotations

import json
from pathlib import Path

OUT = Path(__file__).resolve().parent


def load(name):
    p = OUT / name
    return json.loads(p.read_text()) if p.exists() else None


def main():
    gate = load("HQ2V_gate.json")
    h1 = load("HQ2_1_jeff.json")
    h2 = load("HQ2_2_geff.json")
    h3 = load("HQ2_3_cosmology.json")
    h4 = load("HQ2_4_sigma8.json")
    if not all([gate, h1, h2, h3, h4]):
        missing = [n for n, x in [("V", gate), ("1", h1), ("2", h2),
                                  ("3", h3), ("4", h4)] if x is None]
        print(f"missing artifacts: {missing} -- run the chain first.")
        return 1

    Jc = gate["Jc_O3"]
    cA = h1["route_A"]["c_BD"]
    cB_soft = h1["route_B"]
    c0 = h1["c0"]
    ZBmin = h2["Z_B_min"]
    ZA = h2["Z_A"]
    near = h3["near_Jc_accessible_z0p5"]
    s8 = h4["sigma8_HQ2_fiducial"]
    s8L = h4["sigma8_LCDM"]
    s8K = h4["sigma8_KiDS"]
    J0fid = h4["J0_over_Jc_fiducial"]

    # ---- pre-registered verdict ----
    gate_ok = gate["passed"]
    routeA_const = h1["route_A"]["constant_in_J"]
    routeB_soft = cB_soft["softens_toward_Jc"]
    routeB_suppr = h2["routeB_suppresses"]
    success = h4["success"]
    # Verdict logic (charter sec 5):
    #   A (SUCCESS): suppression real AND accessible AND sigma8 toward KiDS
    #   B (PARTIAL): suppression exists AND plausibly acts at z~0.5-1 but sigma8 short
    #   C (DEATH)  : c_eff const (Route A) OR near-critical regime inaccessible
    if success and near:
        verdict, label = "A", "CRITICAL FLUCTUATIONS RESOLVE S8 (triple-verify!)"
    elif routeB_suppr and near and not success:
        verdict, label = "B", "suppression acts but sigma8 shift insufficient"
    else:
        verdict, label = "C", ("DEATH -- suppression exists but is observationally "
                               "forbidden (constant c pins J0/Jc>>1); and the BD "
                               "photon speed is J-blind. S8 not resolved.")

    md = []
    md.append("# HQ2-5 — Synthesis & Verdict: critical-fluctuation S8 mechanism\n")
    md.append("> Generated by `HQ2_5_synthesis.py` from the HQ2-V…HQ2-4 artifacts. "
              "Verdict by the pre-registered criteria in `HQ2_CRITICAL_FERROMAGNET.md`.\n")

    md.append("## Synthesis table\n")
    md.append("```")
    md.append("HQ2-V (gate):")
    md.append(f"  xi diverges across J_c?                {gate['checks']['xi_diverges']}"
              f"  (growth x{gate['checks']['xi_growth_factor']:.1f})")
    md.append(f"  chi peaks near J_c (interior)?         "
              f"{gate['checks']['chi_interior_peak'] and gate['checks']['chi_peak_near_Jc']}"
              f"  (peak at J/Jc={gate['chi_peak_over_Jc']:.2f})")
    md.append(f"  transition continuous (2nd order)?     {gate['checks']['continuous_2nd_order']}")
    md.append(f"  GATE PASSED:                           {gate_ok}")
    md.append("")
    md.append("HQ2-1 (c_eff):")
    md.append(f"  Route A (BD/E2, J-blind): c_BD={cA:.4f}, constant in J = {routeA_const}")
    md.append(f"  Route B (stiffness): c_eff softens toward J_c = {routeB_soft}")
    md.append(f"    c_eff(1.02 J_c)/c0 = {cB_soft['c_eff_Jc']/c0:.4f}  (vs Route A {cA/c0:.4f})")
    md.append("")
    md.append("HQ2-2 (G_eff):")
    md.append(f"  Route A: G_eff/G_N = {ZA:.4f} (constant) -> suppression {h2['routeA_suppresses']}")
    md.append(f"  Route B: min G_eff/G_N = {ZBmin:.4f} (<1 near J_c) -> suppression {routeB_suppr}")
    md.append(f"  HQ2-2 positive (Route B): {h2['positive']}")
    md.append("")
    md.append("HQ2-3 (cosmology):")
    md.append(f"  near-J_c accessible at z~0.5-1?         {near}")
    md.append(f"    constant-c bound forces J0/J_c >= {J0fid:.3g} -> "
              f"G_eff/G_N(z=0.5) = "
              f"{h3['constancy_bounds']['alpha (1e-6)']['Z_z0p5']:.6f}")
    md.append("")
    md.append("HQ2-4 (sigma8):")
    md.append(f"  sigma8(HQ2) at physical J0/Jc = {s8:.4f}")
    md.append(f"    LCDM = {s8L:.3f} ; KiDS ~ {s8K:.3f}")
    md.append(f"  closer to KiDS than LCDM?               {success}")
    md.append("```\n")

    md.append("## Verdict\n")
    md.append(f"**[{verdict}] — {label}**\n")
    if verdict == "C":
        md.append(
            "The campaign dies, but **not** where FM1–FM4 died. Two independent, "
            "anti-circular routes converge:\n\n"
            "1. **Route A (literal, BD/E2 motor).** The photon speed comes from the "
            "Benincasa–Dowker causal-set d'Alembertian, built from the causal "
            f"*order matrix* alone. The spin coupling `J` is **not** an input, so "
            f"`c_BD = {cA:.3f}` is J-independent *by construction* — the prompt's "
            "literal death criterion (`c_eff constant in J ⇒ Verdict C`). The "
            "E2 photon does **not** soften at criticality.\n\n"
            "2. **Route B (generous, stiffness motor).** Granting the hypothesis its "
            "best case, the mean-field orientation stiffness `ρ_s ∝ m(J)²` **does** "
            f"soften near `J_c` (min `G_eff/G_N = {ZBmin:.3f}` < 1). But HQ2-3 shows "
            "this regime is **observationally forbidden**: a stable photon with "
            "constant `c` (E2, Verdict A) requires `m ≈ m_sat`, i.e. the vacuum sits "
            f"deep in the ordered phase, `J0/J_c ≳ {J0fid:.2g}`. Since `J ∝ a⁻³` "
            "grows into the past, the S8 window (z~0.3–1) is **more** ordered than "
            f"today, so `G_eff/G_N(z=0.5) ≈ 1` and `σ8(HQ2) ≈ σ8(ΛCDM) = {s8:.3f}` — "
            "unmoved toward KiDS.\n\n"
            "**The structural obstruction (new):** the very existence of the photon "
            "(deep order, constant c) *forbids* the near-critical regime that S8 "
            "would need. Critical softening is real but unreachable. This is a "
            "third, distinct reason S8 resists the TEIC+DEV vacuum — separate from "
            "the mean-field `μ≥1` enhancement (FM1) and the Goldstone/massive "
            "structure (FM2–FM4).\n")
        md.append("\n**Positive residue.** The critical softening of the orientation "
                  "stiffness (ν≈0.71, O(3) 3D) is genuine and is the same critical "
                  "physics that supplies ν_MOND (FM2-1). HQ2 maps *where* it lives: "
                  "only at `J≈J_c`, a regime the photon excludes.\n")
        md.append("\n**Triple verification (V1–V3): NOT triggered** — reserved for "
                  "Verdict A only. No S8 resolution is claimed.\n")
    elif verdict == "B":
        md.append("Suppression exists and could act in the S8 window, but the σ8 "
                  "shift is insufficient to reach KiDS. Recorded as a promising "
                  "direction; not a resolution.\n")
    else:
        md.append("Critical fluctuations suppress G_eff, the universe could have "
                  "been near J_c in the S8 window, and σ8 moves toward KiDS. "
                  "**Triple verification (V1 finite-size, V2 protocol, V3 two-method "
                  "σ8) is now MANDATORY before any claim.**\n")

    md.append("\n## Anti-circularity ledger\n")
    md.append("- `G_N` never entered as a constant; `G_eff/G_N = Z` is a measured "
              "`(c_eff/c0)²` lattice ratio.\n")
    md.append("- `J_c = 0.08` is the E1-2 χ-peak value, never tuned to dodge death.\n")
    md.append("- BD operator (Route A) has no `J`, no `c` insert; `c` is a free fit.\n")
    md.append("- KiDS/ΛCDM σ8 are COMPARISON ONLY.\n")
    md.append("- No previous campaign (E1, E2, FM1–FM4, FN*, HE*, HQ3) was modified.\n")

    (OUT / "HQ2_5_synthesis.md").write_text("\n".join(md), encoding="utf-8")
    print(f"VERDICT [{verdict}]: {label}")
    print(f"saved {OUT/'HQ2_5_synthesis.md'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
