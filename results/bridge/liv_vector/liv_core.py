def boost_matrix(eta, m):
    """Contravariant boost Lambda(eta, m_hat) in D=4, x'^a = Lambda^a_b x^b."""
    m = np.asarray(m, float)
    m = m / np.linalg.norm(m)
    L = np.eye(4)
    ch, sh = np.cosh(eta), np.sinh(eta)
    L[0, 0] = ch
    L[0, 1:] = sh * m
    L[1:, 0] = sh * m
    L[1:, 1:] += (ch - 1.0) * np.outer(m, m)
    return L


def boost_bivectors(Om, eta, rng):
    """Boost each bivector by rapidity eta along an independent random direction."""
    n = len(Om)
    m = rng.normal(size=(n, 3))
    m /= np.linalg.norm(m, axis=1, keepdims=True)
    ch, sh = np.cosh(eta), np.sinh(eta)
    L = np.tile(np.eye(4), (n, 1, 1))
    L[:, 0, 0] = ch
    L[:, 0, 1:] = sh * m
    L[:, 1:, 0] = sh * m
    L[:, 1:, 1:] += (ch - 1.0) * np.einsum("ni,nj->nij", m, m)
    return np.einsum("nab,nbc,ndc->nad", L, Om, L)


def eb_of(Om):
    """(e2, b2) per bivector for a (n,4,4) stack."""
    e2 = np.sum(Om[:, 0, 1:] ** 2, axis=1)
    iu = np.triu_indices(3, k=1)
    b2 = np.sum(Om[:, 1:, 1:][:, iu[0], iu[1]] ** 2, axis=1)
    return e2, b2


