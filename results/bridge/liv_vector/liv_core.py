# Plaquette ensemble (same generator + window as W2: margin 0.25, base cap 4)
# --------------------------------------------------------------------------- #
def plaquette_ensemble(rho, extent, n_real, seed0, n_bases=1500, margin=0.25,
                       max_per_base=4, D=4):
    """Sprinkle, build causal-diamond loops, return per-seed plaquette arrays.

    Each seed dict carries:
      Om   (n,D,D) contravariant area bivectors
      e2   (n,)    sum_i (Om^{0i})^2          [electric-plane weight]
      b2   (n,)    sum_{i<j} (Om^{ij})^2      [magnetic-plane weight]
      pa2  (n,)    e2 - b2 = squared proper area (Lorentz invariant, > 0)
      eta  (n,)    axis rapidity of the timelike diagonal d1 = l - p
      vol          inner-window coordinate volume
    """
    bounds = np.array([[0.0, float(extent)]] * D)
    lo = bounds[:, 0] + margin * (bounds[:, 1] - bounds[:, 0])
    hi = bounds[:, 1] - margin * (bounds[:, 1] - bounds[:, 0])
    vol = float(np.prod(hi - lo))
    seeds = []
    for s in range(n_real):
        rng = np.random.default_rng(seed0 + s)
        pts = sprinkle_box(rho, bounds, rng)
        if len(pts) < 8:
            continue
        loops = causal_diamond_loops(pts, max_per_base=max_per_base,
                                     n_bases=n_bases, rng=rng)
        if not loops:
            continue
        V = np.asarray(loops, float)               # (n, 4, D): p, j, l, k
        c = V.mean(axis=1)
        keep = np.all((c >= lo) & (c <= hi), axis=1)
        V = V[keep]
        if len(V) == 0:
            continue
        p, j, l, k = V[:, 0], V[:, 1], V[:, 2], V[:, 3]
        d1 = l - p                                  # timelike diagonal
        d2 = k - j                                  # spacelike chord
        Om = 0.5 * (np.einsum("ni,nj->nij", d1, d2)
                    - np.einsum("ni,nj->nij", d2, d1))
        e2 = np.sum(Om[:, 0, 1:] ** 2, axis=1)
        iu = np.triu_indices(D - 1, k=1)
        b2 = np.sum(Om[:, 1:, 1:][:, iu[0], iu[1]] ** 2, axis=1)
        sp = np.linalg.norm(d1[:, 1:], axis=1)
        eta = np.arctanh(np.clip(sp / d1[:, 0], 0.0, 1.0 - 1e-12))
        seeds.append({"Om": Om, "e2": e2, "b2": b2, "pa2": e2 - b2,
                      "eta": eta, "vol": vol, "verts": V})
    return seeds


def pooled(seeds, key):
    return np.concatenate([s[key] for s in seeds]) if seeds else np.array([])


