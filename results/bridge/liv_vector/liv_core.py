def F_pure_E(E0, axis=1):
    """Covariant F_{mn} of a pure electric field of strength E0 along `axis`."""
    F = np.zeros((4, 4))
    F[0, axis] = E0
    F[axis, 0] = -E0
    return F


def boost_field(F, beta, m):
    """Boost-equivalent field: F'_{mn} = Lambda(-beta)^a_m Lambda(-beta)^b_n F_{ab}.

    For a boost, Lambda is symmetric and Lambda^{-1} = Lambda(-beta), so the
    covariant transform is F' = Lambda(-beta) F Lambda(-beta).  Lorentz
    invariants F:F^ and F:~F^ are conserved exactly (asserted by callers).
    """
    Li = boost_matrix(-beta, m)
    return Li @ F @ Li


def F_invariant(F):
    """F_{mn} F^{mn} = 2 (B^2 - E^2), signature (+,-,-,-)."""
    eta = np.diag([1.0, -1.0, -1.0, -1.0])
    return float(np.tensordot(F, eta @ F @ eta, axes=2))


def action_sum(F, Om):
    """S = sum_p [1 - cos W_p],  W_p = (1/2) F_{mn} Omega^{mn} (exact, W1)."""
    W = 0.5 * np.einsum("mn,pmn->p", F, Om)
    return float(np.sum(1.0 - np.cos(W))), W


# self-check: the quadrilateral identity Omega = 1/2 d1^d2 vs area_bivector
def _self_check():
    rng = np.random.default_rng(0)
    verts = rng.normal(size=(4, 4))
    A = area_bivector(verts)
    d1, d2 = verts[2] - verts[0], verts[3] - verts[1]
    B = 0.5 * (np.outer(d1, d2) - np.outer(d2, d1))
    assert np.allclose(A, B, atol=1e-12), "quadrilateral bivector identity failed"


_self_check()
